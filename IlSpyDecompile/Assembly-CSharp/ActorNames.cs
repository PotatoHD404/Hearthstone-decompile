	public static string GetZoneActor(TAG_CARDTYPE cardType, TAG_CLASS classTag, TAG_ZONE zoneTag, Player controller, TAG_PREMIUM premium, bool isQuest, bool isSideQuest, bool isSigil, bool isGhostly, bool isPuzzle, TAG_PUZZLE_TYPE puzzleType, bool isRulebook, bool isSidekickHero)
	{
		switch (zoneTag)
		{
		case TAG_ZONE.DECK:
		case TAG_ZONE.REMOVEDFROMGAME:
		case TAG_ZONE.SETASIDE:
			return "Card_Invisible.prefab:579b3b9a80234754593f24582f9cb93b";
		case TAG_ZONE.SECRET:
			if (isQuest)
			{
				return "Card_Play_Quest.prefab:321b6d1ad558ebd46996c1f4eeaccb0c";
			}
			if (isPuzzle)
			{
				switch (puzzleType)
				{
				case TAG_PUZZLE_TYPE.MIRROR:
					return "Card_Play_Puzzle_Mirror.prefab:4583d6e2b04fad74986ef47b4ff00c79";
				case TAG_PUZZLE_TYPE.LETHAL:
					return "Card_Play_Puzzle_Lethal.prefab:00d669c10a286e84cb91df1d40312d4b";
				case TAG_PUZZLE_TYPE.SURVIVAL:
					return "Card_Play_Puzzle_Survival.prefab:036a2c2eee552fc4db25051107a0b797";
				case TAG_PUZZLE_TYPE.CLEAR:
					return "Card_Play_Puzzle_BoardClear.prefab:fd9eec17f48c319468f103336095ad7b";
				}
			}
			if (isRulebook)
			{
				return "Card_Play_Rulebook.prefab:a8fbb8b315f4a3244be82718c1606858";
			}
			if (isSideQuest)
			{
				switch (classTag)
				{
				case TAG_CLASS.DRUID:
					return "Card_Play_SideQuest_Druid.prefab:d1430dc4bc9786640a02f4b178b59393";
				case TAG_CLASS.HUNTER:
					return "Card_Play_SideQuest_Hunter.prefab:c9ed37b5a056d4e4885dc882d9d37664";
				case TAG_CLASS.MAGE:
					return "Card_Play_SideQuest_Mage.prefab:39faefe5a4f9cf54ba9d85deb7627acb";
				case TAG_CLASS.PALADIN:
					return "Card_Play_SideQuest_Paladin.prefab:396bf10a7c7da404ea3624e009861780";
				case TAG_CLASS.ROGUE:
					return "Card_Play_SideQuest_Rogue.prefab:e805c70aa076e6743925e8d06a4be247";
				}
			}
			if (isSigil && classTag == TAG_CLASS.DEMONHUNTER)
			{
				return "Card_Play_Sigil_DemonHunter.prefab:b1ee048f6f0150e4ebd512208fb6a707";
			}
			return classTag switch
			{
				TAG_CLASS.HUNTER => "Card_Play_Secret_Hunter.prefab:fdf71d0657e17a7428a43c1a8f319818", 
				TAG_CLASS.MAGE => "Card_Play_Secret_Mage.prefab:ffc78954f637f6f4d8b8bb7ec0b936ca", 
				TAG_CLASS.PALADIN => "Card_Play_Secret_Paladin.prefab:b0f3901ff0fad674bb7c72faa7966e73", 
				TAG_CLASS.ROGUE => "Card_Play_Secret_Rogue.prefab:1b224ad272f03724c9bc0aa802456c3e", 
				TAG_CLASS.WARRIOR => "Card_Play_Secret_Wanderer.prefab:9eaa9bf6015f05f4e9bbe9ba5e42b20f", 
				_ => "Card_Play_Secret_Mage.prefab:ffc78954f637f6f4d8b8bb7ec0b936ca", 
			};
		case TAG_ZONE.PLAY:
		{
			string playActor = GetPlayActor(cardType, premium);
			if (!"Card_Hidden.prefab:1a94649d257bc284ca6e2962f634a8b9".Equals(playActor))
			{
				return playActor;
			}
			break;
		}
		case TAG_ZONE.HAND:
			if (controller != null && controller.IsRevealed())
			{
				string handActor = GetHandActor(cardType, premium, isSidekickHero);
				if (!"Card_Hidden.prefab:1a94649d257bc284ca6e2962f634a8b9".Equals(handActor))
				{
					return handActor;
				}
				break;
			}
			return "Card_Hidden.prefab:1a94649d257bc284ca6e2962f634a8b9";
		case TAG_ZONE.GRAVEYARD:
			if (isGhostly && controller.GetSide() == Player.Side.OPPOSING)
			{
				return "Card_Hidden.prefab:1a94649d257bc284ca6e2962f634a8b9";
			}
			switch (cardType)
			{
			case TAG_CARDTYPE.MINION:
				return GetNameWithPremiumType(ACTOR_ASSET.HAND_MINION, premium);
			case TAG_CARDTYPE.WEAPON:
				return GetNameWithPremiumType(ACTOR_ASSET.HAND_WEAPON, premium);
			case TAG_CARDTYPE.SPELL:
				return GetNameWithPremiumType(ACTOR_ASSET.HAND_SPELL, premium);
			case TAG_CARDTYPE.HERO:
				return GetNameWithPremiumType(ACTOR_ASSET.HAND_HERO, premium);
			}
			break;
		}
		Debug.LogWarningFormat("ActorNames.GetZoneActor() - Can't determine actor for {0}. Returning {1} instead.", cardType, "Card_Hidden.prefab:1a94649d257bc284ca6e2962f634a8b9");
		return "Card_Hidden.prefab:1a94649d257bc284ca6e2962f634a8b9";
	}

	private static bool ShouldObfuscate(Entity entity)
	{
		if (entity.GetController() != null && !entity.GetController().IsFriendlySide() && entity.IsObfuscated())
		{
			return true;
		}
		return false;
	}

	public static string GetZoneActor(Entity entity, TAG_ZONE zoneTag)
	{
		if (ShouldObfuscate(entity) && zoneTag == TAG_ZONE.PLAY)
		{
			return "Card_Play_Obfuscated.prefab:682f46c64054e9948875d38245cbacae";
		}
		if (entity.IsHero() && zoneTag == TAG_ZONE.GRAVEYARD)
		{
			return GetGraveyardActorForHero(entity);
		}
		return GetZoneActor(entity.GetCardType(), entity.GetClass(), zoneTag, entity.GetController(), entity.GetPremiumType(), entity.IsQuest(), entity.IsSideQuest(), entity.IsSigil(), entity.HasTag(GAME_TAG.GHOSTLY), entity.IsPuzzle(), entity.GetPuzzleType(), entity.IsRulebook(), entity.IsSidekickHero());
	}

	public static string GetZoneActor(EntityDef entityDef, TAG_ZONE zoneTag)
	{
		return GetZoneActor(entityDef.GetCardType(), entityDef.GetClass(), zoneTag, null, TAG_PREMIUM.NORMAL, entityDef.IsQuest(), entityDef.IsSideQuest(), entityDef.IsSigil(), entityDef.HasTag(GAME_TAG.GHOSTLY), entityDef.IsPuzzle(), entityDef.GetPuzzleType(), entityDef.IsRulebook(), entityDef.IsSidekickHero());
	}

	public static string GetZoneActor(EntityDef entityDef, TAG_ZONE zoneTag, TAG_PREMIUM premium)
	{
		return GetZoneActor(entityDef.GetCardType(), entityDef.GetClass(), zoneTag, null, premium, entityDef.IsQuest(), entityDef.IsSideQuest(), entityDef.IsSigil(), entityDef.HasTag(GAME_TAG.GHOSTLY), entityDef.IsPuzzle(), entityDef.GetPuzzleType(), entityDef.IsRulebook(), entityDef.IsSidekickHero());
	}

	private static string GetGraveyardActorForHero(Entity entity)
	{
		Card card = entity.GetCard();
		if (entity.IsHero() && card != null && card.GetPrevZone() is ZoneHero)
		{
			return GetNameWithPremiumType(ACTOR_ASSET.PLAY_HERO, TAG_PREMIUM.NORMAL);
		}
		return GetZoneActor(entity.GetCardType(), entity.GetClass(), TAG_ZONE.GRAVEYARD, entity.GetController(), entity.GetPremiumType(), entity.IsQuest(), entity.IsSideQuest(), entity.IsSigil(), entity.HasTag(GAME_TAG.GHOSTLY), entity.IsPuzzle(), entity.GetPuzzleType(), entity.IsRulebook(), entity.IsSidekickHero());
	}

	public static string GetHandActor(TAG_CARDTYPE cardType, TAG_PREMIUM premiumType, bool isSidekickHero = false)
	{
		switch (cardType)
		{
		case TAG_CARDTYPE.MINION:
			return GetNameWithPremiumType(ACTOR_ASSET.HAND_MINION, premiumType);
		case TAG_CARDTYPE.WEAPON:
			return GetNameWithPremiumType(ACTOR_ASSET.HAND_WEAPON, premiumType);
		case TAG_CARDTYPE.SPELL:
			return GetNameWithPremiumType(ACTOR_ASSET.HAND_SPELL, premiumType);
		case TAG_CARDTYPE.HERO:
			if (isSidekickHero)
			{
				return GetNameWithPremiumType(ACTOR_ASSET.SIDEKICK_HERO, premiumType);
			}
			return GetNameWithPremiumType(ACTOR_ASSET.HAND_HERO, premiumType);
		case TAG_CARDTYPE.HERO_POWER:
			return GetNameWithPremiumType(ACTOR_ASSET.HISTORY_HERO_POWER, premiumType);
		default:
			return "Card_Hidden.prefab:1a94649d257bc284ca6e2962f634a8b9";
		}
	}

	public static string GetHandActor(TAG_CARDTYPE cardType)
	{
		return GetHandActor(cardType, TAG_PREMIUM.NORMAL);
	}

	public static string GetHandActor(Entity entity)
	{
		return GetHandActor(entity.GetCardType(), entity.GetPremiumType(), entity.IsSidekickHero());
	}

	public static string GetHandActor(EntityDef entityDef)
	{
		return GetHandActor(entityDef.GetCardType(), TAG_PREMIUM.NORMAL, entityDef.IsSidekickHero());
	}

	public static string GetHandActor(EntityDef entityDef, TAG_PREMIUM premiumType)
	{
		return GetHandActor(entityDef.GetCardType(), premiumType, entityDef.IsSidekickHero());
	}

	public static string GetHeroSkinOrHandActor(TAG_CARDTYPE type, TAG_PREMIUM premium)
	{
		if (type == TAG_CARDTYPE.HERO)
		{
			return "Card_Hero_Skin.prefab:ed2af57fa6b571741ab047c2c3e0e663";
		}
