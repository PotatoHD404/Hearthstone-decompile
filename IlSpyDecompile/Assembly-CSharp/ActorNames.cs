	public static string GetNameWithPremiumType(ACTOR_ASSET actorName, TAG_PREMIUM premiumType)
	{
		string value = null;
		if (premiumType != TAG_PREMIUM.GOLDEN)
		{
			if (premiumType != TAG_PREMIUM.DIAMOND)
			{
				goto IL_002c;
			}
			if (s_diamondActorAssets.TryGetValue(actorName, out value))
			{
				return value;
			}
		}
		if (s_premiumActorAssets.TryGetValue(actorName, out value))
		{
			return value;
		}
		goto IL_002c;
		IL_002c:
		if (s_actorAssets.TryGetValue(actorName, out value))
		{
			return value;
		}
		return null;
	}
