	public static string GetHistoryActor(Entity entity, HistoryInfoType historyTileType)
	{
		if (entity.IsSecret() && entity.IsHidden())
		{
			return GetHistorySecretActor(entity);
		}
		if (ShouldObfuscate(entity))
		{
			return "History_Obfuscated.prefab:d620dfa4ff929274d8805efec62fc096";
		}
		if (string.IsNullOrEmpty(entity.GetCardId()))
		{
			return "Card_Hidden.prefab:1a94649d257bc284ca6e2962f634a8b9";
		}
		TAG_CARDTYPE cardType = entity.GetCardType();
		TAG_PREMIUM premiumType = entity.GetPremiumType();
		switch (cardType)
		{
		case TAG_CARDTYPE.HERO_POWER:
			if (entity.GetController().IsFriendlySide())
			{
				return GetNameWithPremiumType(ACTOR_ASSET.HISTORY_HERO_POWER, premiumType);
			}
			return GetNameWithPremiumType(ACTOR_ASSET.HISTORY_HERO_POWER_OPPONENT, premiumType);
		case TAG_CARDTYPE.HERO:
			if (entity.GetZone() != TAG_ZONE.PLAY || historyTileType == HistoryInfoType.CARD_PLAYED)
			{
				if (entity.IsSidekickHero())
				{
					return GetNameWithPremiumType(ACTOR_ASSET.SIDEKICK_HERO, premiumType);
				}
				return GetNameWithPremiumType(ACTOR_ASSET.HAND_HERO, premiumType);
			}
			if (entity.IsSidekickHero())
			{
				if (entity.GetController().IsFriendlySide())
				{
					return GetNameWithPremiumType(ACTOR_ASSET.SIDEKICK_HERO, premiumType);
				}
				return GetNameWithPremiumType(ACTOR_ASSET.SIDEKICK_HERO_OPPONENT, premiumType);
			}
			return "History_Hero.prefab:a040b63fa76fd4348b2a41b3bdc9789c";
		default:
			return GetHandActor(entity);
		}
	}

